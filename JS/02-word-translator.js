let language = prompt ('Enter the code for the language you would like. (en, fr, es, de)');

if (language === 'en') {
    console.log('Hello World');
} else if (language === 'fr') {
    console.log('Salut tout le monde')
} else if (language === 'es') {
    console.log('Hola mundo')
} else if (language === 'de') {
    console.log('Hallo Welt')
} else
console.log('Hello World');